CREATE TYPE lottery.PRIZE_STATUS_ENUM AS ENUM ('ACTIVE', 'SUSPENDED');
CREATE SEQUENCE IF NOT EXISTS lottery.prize_seq START 501;
DROP TABLE IF EXISTS lottery.prize;
CREATE TABLE IF NOT EXISTS lottery.prize (
  id                 INTEGER                   NOT NULL DEFAULT nextval('lottery.prize_seq' :: REGCLASS),
  code               VARCHAR(255)              NOT NULL UNIQUE,
  prize_schema_id    INTEGER                   NOT NULL,
  scheduler_id       INTEGER                   NOT NULL,
  prize_position     INTEGER                   NOT NULL,
  status             lottery.PRIZE_STATUS_ENUM NOT NULL DEFAULT 'ACTIVE' :: lottery.PRIZE_STATUS_ENUM,

  version            BIGINT                             DEFAULT 0,
  created_date       TIMESTAMP                          DEFAULT CURRENT_TIMESTAMP,
  last_modified_date TIMESTAMP                          DEFAULT CURRENT_TIMESTAMP,
  created_by         VARCHAR(255),
  last_modified_by   VARCHAR(255),

  PRIMARY KEY (id),
  FOREIGN KEY (prize_schema_id) REFERENCES lottery.prize_schema(id),
  FOREIGN KEY (scheduler_id) REFERENCES lottery.scheduler(id),
  UNIQUE (prize_schema_id, scheduler_id),
  CHECK (prize_position > 0)
);

