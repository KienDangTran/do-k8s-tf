  id                 INTEGER                     NOT NULL DEFAULT nextval('lottery.lottery_seq' :: REGCLASS),
  code               VARCHAR(255)                NOT NULL UNIQUE,
  lottery_schema_id  INTEGER                     NOT NULL,
  scheduler_id       INTEGER                     NOT NULL,
  bet_unit_price     NUMERIC(15, 2),
  win_unit_price     NUMERIC(15, 2),
  max_bet_item       INTEGER,
  bet_item_max_amt   NUMERIC(15, 2),
  max_payout         NUMERIC(15, 2),
  status             lottery.LOTTERY_STATUS_ENUM NOT NULL DEFAULT 'ACTIVE' :: lottery.LOTTERY_STATUS_ENUM,

