CREATE SEQUENCE IF NOT EXISTS lottery.ticket_seq START 101;
DROP TABLE IF EXISTS lottery.ticket;
CREATE TABLE IF NOT EXISTS lottery.ticket (
  id                        BIGINT                     NOT NULL DEFAULT nextval('lottery.ticket_seq' :: REGCLASS),
  issue_id                  BIGINT                     NOT NULL,
  lottery_id                INTEGER                    NOT NULL,
  ccy                       VARCHAR(5)                 NOT NULL,
  username                  VARCHAR(255)               NOT NULL,
  device_registration_token TEXT,
  total_bet_item            INTEGER                    NOT NULL,
  total_bet_unit            INTEGER                    NOT NULL,
  total_bet_amt             NUMERIC(15, 2)             NOT NULL,
  total_payout              NUMERIC(15, 2)             NOT NULL,
  total_won_unit            INTEGER                    NOT NULL,
  status                    lottery.TICKET_STATUS_ENUM NOT NULL DEFAULT 'NEW' :: lottery.TICKET_STATUS_ENUM,

  version                   BIGINT                              DEFAULT 0,
  created_date              TIMESTAMP                           DEFAULT CURRENT_TIMESTAMP,
  last_modified_date        TIMESTAMP                           DEFAULT CURRENT_TIMESTAMP,
  created_by                VARCHAR(255),
  last_modified_by          VARCHAR(255),

  PRIMARY KEY (id),
  FOREIGN KEY (issue_id) REFERENCES lottery.issue(id),
  FOREIGN KEY (lottery_id) REFERENCES lottery.lottery(id),
  UNIQUE (issue_id, lottery_id, ccy, username),
  CHECK (
      total_bet_item > 0
      AND total_bet_unit > 0
      AND total_bet_amt > 0
      AND total_payout >= 0
      AND total_won_unit >= 0
    )
);

