  created_date       TIMESTAMP                            DEFAULT CURRENT_TIMESTAMP,
  last_modified_date TIMESTAMP                            DEFAULT CURRENT_TIMESTAMP,
  created_by         VARCHAR(255),
  last_modified_by   VARCHAR(255),

  PRIMARY KEY (id),
  FOREIGN KEY (lottery_schema_id) REFERENCES lottery.lottery_schema(id),
  FOREIGN KEY (scheduler_id) REFERENCES lottery.scheduler(id),
  UNIQUE (scheduler_id, lottery_schema_id),
  CHECK (
      bet_unit_price > 0
      AND win_unit_price > 0
      AND max_bet_item > 0
      AND bet_item_max_amt > 0
      AND max_payout > 0
    )
);

